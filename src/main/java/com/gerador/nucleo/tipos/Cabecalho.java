package com.gerador.nucleo.tipos;

public class Cabecalho {
	private String incio;
	private String nome;
	private String tExtends;
	private String tImplements;
	private String fim;
	public String getIncio() {
		return incio;
	}
	public void setIncio(String incio) {
		this.incio = incio;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String gettExtends() {
		return tExtends;
	}
	public void settExtends(String tExtends) {
		this.tExtends = tExtends;
	}
	public String gettImplements() {
		return tImplements;
	}
	public void settImplements(String tImplements) {
		this.tImplements = tImplements;
	}
	public String getFim() {
		return fim;
	}
	public void setFim(String fim) {
		this.fim = fim;
	}
		
	
}
